# Function to draw a diamond shape
def draw_diamond(turtle, size):
    for _ in range(2):
        turtle.forward(size)
        turtle.right(60)
        turtle.forward(size)
        turtle.right(120)

# Function to draw the gradient ring with n number of diamond shapes
def draw_gradient_ring(turtle, n, size):
    ring.fillcolor("yellow")
    angle = 360 / n
    for i in range(n):
        ring.begin_fill()
        # Create a color gradient
        hue = i / n  # Map the index to a hue value
        color = colorsys.hsv_to_rgb(hue, 1, 1)  # Convert the hue to RGB
        turtle.pencolor(color)  # Set the pen color
        draw_diamond(turtle, size)
        turtle.right(angle)
        ring.end_fill()

# Function to draw a circle of gradient rings
def draw_circle_of_rings(turtle, num_rings, radius):
    # angle = 360 / num_rings
    turtle.left(70)
    for i in range(num_rings):
        turtle.circle(radius,12)
        draw_gradient_ring(turtle, 5, 10)
        turtle.circle(radius,12)

# Function to draw the ring with n number of diamond shapes
def draw_ring(turtle, n, size,):
    angle = 360 / n
    for _ in range(n):
        draw_diamond(turtle, size)
        turtle.right(angle)
