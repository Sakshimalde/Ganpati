#RAYS
screen.colormode(255)
ray_turtle = turtle.Turtle()
ray_turtle.speed(0)  # fastest speed
ray_turtle.hideturtle()

# Define the origin point for the rays
origin_x, origin_y = -210.77,-35.33
num_rays = 25
ray_length = 300

# Define the  color
orange_color = (255, 160, 122)  
yellow_light_color = (242, 198, 100) 

# animation function
def animate(frame):
    ray_turtle.clear()
    for i in range(num_rays):
        angle = -3 * math.pi / 4 + i * math.pi / 2 / num_rays  # start from -135 degrees, quarter circle
        x = origin_x + ray_length * math.cos(angle)
        y = origin_y + ray_length * math.sin(angle)
        ray_turtle.penup()
        ray_turtle.goto(origin_x, origin_y)
        ray_turtle.pendown()
        ray_turtle.color(orange_color)
        ray_turtle.begin_fill()
        ray_turtle.goto(x, y)
        ray_turtle.end_fill()
        ray_turtle.color(yellow_light_color)
        ray_turtle.begin_fill()
        ray_turtle.goto(x - 5, y - 5)  # shadow offset
        ray_turtle.goto(x + 5, y + 5)  # shadow offset
        ray_turtle.goto(origin_x + 5, origin_y + 5)  # shadow offset
        ray_turtle.goto(origin_x - 5, origin_y - 5)  # shadow offset
        ray_turtle.end_fill()

# Create the animation
for frame in range(100):
    if stop_loop:
        break
    animate(frame) 
    screen.update()
def stop_function():
   exit()
screen.onkey(stop_function, "space")  # Stop the function when the space key is pressed
screen.listen()  


